function transliterate(input) {
  let out = "";
  let i = 0;

  while (i < input.length) {
    let match = "";

    for (let len = 3; len > 0; len--) {
      let part = input.substr(i, len).toLowerCase();
      if (syllables[part]) {
        match = syllables[part];
        i += len;
        break;
      }
    }

    if (match) {
      out += match;
    } else {
      out += input[i];
      i++;
    }
  }

  return out;
}
